# Lists ===>> stores data which are related to each other

lists_array_fruits = ["cherry", "mango", "apple"]

# print list item
print(lists_array_fruits[0])
print(lists_array_fruits[-1]) # last element

# change the value
lists_array_fruits[0] = "banana"
print(lists_array_fruits[0])

# add items ==>> added at the end of the list ==>> variable.append("value/item")
lists_array_fruits.append("Grapes")
print(lists_array_fruits[-1]) # last element
