# subscripting.

# It means using square brackets [] with an index number to get a specific character from the string.
print("Hello"[0])
# Calling or accessing any element from its location (index) in a string is called subscripting.

# 0 to (n-1) ==>> checks from beginning i.e from H in "hello" ===>>> asending order
# -1 to -n ==>> checks from ending i.e from o in "hello" ===>>> desending order

x = int(input("Enter the position : "))
print("Hello"[x])
